/** Weather conditions that use storm/overcast sky gradients. */
const STORM_WEATHER = new Set([
  "cloudy",
  "fog",
  "rain",
  "snow",
  "wind",
  "blizzard",
]);

/**
 * Returns a CSS `linear-gradient(...)` string for the sky background
 * based on time of day and current weather.
 */
export function getSkyGradient(timeOfDay: TimeOfDay, weather: string): string {
  const table = STORM_WEATHER.has(weather) ? STORM_SKY : CLEAR_SKY;
  return table[timeOfDay];
}
