// ---------------------------------------------------------------------------
// Helper: waypoint index -> terrain band
// ---------------------------------------------------------------------------

const WAYPOINT_BAND_MAP: TerrainBandName[] = [
  "forest", // 0
  "forest", // 1
  "forest", // 2
  "rocky", // 3
  "rocky", // 4
  "rocky", // 5
  "plateau", // 6
  "plateau", // 7
  "plateau", // 8
  "storm", // 9
  "storm", // 10
  "storm", // 11
  "summit", // 12
];

export function getTerrainBand(waypointIndex: number): TerrainBandName {
  if (waypointIndex < 0) return "forest";
  if (waypointIndex >= WAYPOINT_BAND_MAP.length) return "summit";
  return WAYPOINT_BAND_MAP[waypointIndex];
}

