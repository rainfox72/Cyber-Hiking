/**
 * Terrain profile data for the MountainScene parallax renderer.
 *
 * Each of the 5 terrain bands defines 5 layers of ridge silhouettes
 * (normalized 0-1 coordinate space) plus color palettes for 4 lighting
 * conditions. A helper maps waypoints to bands and another returns
 * sky-gradient CSS strings for each time-of-day / weather combo.
 */

import type { TimeOfDay } from "../engine/types.ts";

// ---------------------------------------------------------------------------
// Types
// ---------------------------------------------------------------------------

export type TerrainBandName =
  | "forest"
  | "rocky"
  | "plateau"
  | "storm"
  | "summit";

/** Normalized [x, y] pairs. x: 0-1 left-right, y: 0-1 top-bottom. */
export type RidgePoints = [number, number][];

export interface TerrainProfile {
  band: TerrainBandName;
  layers: {
    1: RidgePoints; // foreground
    2: RidgePoints; // near terrain
    3: RidgePoints; // primary spine
    4: RidgePoints; // secondary ridge
    5: RidgePoints; // distant range
  };
  colors: {
    base: string[]; // 5 fill colors, index 0 = layer 1 (foreground)
    dawn: string[];
    night: string[];
    storm: string[];
  };
}

